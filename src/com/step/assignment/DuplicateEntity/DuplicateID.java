package com.step.assignment.DuplicateEntity;

public class DuplicateID {
    private String name;
    private int id;

    public DuplicateID(String name, int id) {
        this.name = name;
        this.id = id;
    }


    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }
}
